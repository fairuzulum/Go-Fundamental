package main

import "fmt"

func main() {
	// Operasi Perbandingan
	// Operator perbandingan adalah operator yang digunakan untuk membandingkan dua buah nilai.
	// Hasil dari operasi perbandingan adalah boolean (true atau false).

	// Sama dengan (==)
	var a = 10
	var b = 10
	var c = a == b
	fmt.Println(c)

	name1 := "fairuz"
	name2 := "Fairuz"
	fmt.Println(name1 == name2) // false

	

}